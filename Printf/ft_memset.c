#include "header.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*cs;
	size_t	i;

	cs = (char *)s;
	i = 0;
	while (i < n)
	{
		cs[i] = c;
		i++;
	}
	return (cs);
}
