#include "libft.h"

void		ft_putnbr(int n)
{
	int n1;

	n1 = n;
	if (n == -2147483648)
		n1 = n / 10;
	if (n1 < 0)
	{
		n1 = n1 * (-1);
		ft_putchar('-');
	}
	if (n1 < 10)
		ft_putchar(n1 + '0');
	else
		ft_putnbr(n1 / 10);
	if (n1 >= 10)
		ft_putnbr(n1 % 10);
	if (n == -2147483648)
		ft_putchar(8 + '0');
}
